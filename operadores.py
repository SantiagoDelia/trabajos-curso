#OPERADORES

#Son simbolos o conjuntos de simbolos que realizan una funcion especifica en uno o mas operandos

#--------------------------------------------------------------

#TIPOS DE OPERADORES

#--------------------------------------------------------------

# Aritmeticos: 
# + suma
# - resta
# * multiplicacion
# / division
#  % modulo
#  ** exponente
#  // division entera

#--------------------------------------------------------------

# De Asignacion: 

# = asignacion simple
# += suma y asignacion
# -= resta y asignacion    
# *= multiplicacion y asignacion
#  /= division y asignacion
#  //= division entera y asignacion
#  %= modulo y asignacion 
# **= exponente y asignacion

#--------------------------------------------------------------

# De Comparacion: 
# == (no es lo mismo = que == ya que dos iguales nos sirven para comparar igualdades)
# != diferente
#  > mayor que
#  < menor que   
#  >= mayor o igual que
#  <= menor o igual que

#--------------------------------------------------------------

# Logicos: 
# and (nos va a devolver verdadero si true si ambas afirmaciones son verdaderas)
# or  (nos va a devolver verdadero si almenos alguna de las dos afirmaciones es verdaderas)
# not (nos devolvera el opuesto al valor que siga) 

#ejemplo de not

x = 10

boolean =  (not x == 0) # (estamos diciendo que no es igual a 0)

print (boolean) 


#--------------------------------------------------------------

# De Identidad: 
# is (esta variable es igual a esta otra variable)
# is not (esta variable no es igual a esta otra variable)

#--------------------------------------------------------------

# De Pertenencia: 
# in (este valor esta dentro de esta variable?)
# not in (este valor no esta dentro de esta variable?)

